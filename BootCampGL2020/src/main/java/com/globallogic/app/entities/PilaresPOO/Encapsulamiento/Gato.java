public class Gato {

    private String nombre;
    private int patas;

    public String getNombre() {
        return this.nombre;
    }

    public void setNombre(String nuevoNombre) {
        this.nombre = nuevoNombre;
    }

    public int getPatas() {
        return this.patas;
    }

    public void setPatas(int numeroPatas) {
        this.patas = numeroPatas;
    }
    
    
}